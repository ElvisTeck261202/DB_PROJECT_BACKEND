
DROP TABLE IF EXISTS GYM;
CREATE DATABASE GYM;

USE GYM;



CREATE TABLE ROOMS (
ID_ROOM INTEGER (5) NOT NULL,
LOCATION VARCHAR(50),
SQUARE_METERS DOUBLE (5,2),
TYPE_ROOM VARCHAR (50),
PRIMARY KEY (ID_ROOM) 
);

CREATE TABLE DEVICES(
ID_DEVICE INTEGER (5) NOT NULL,
ID_ROOM INTEGER (5) NOT NULL,
DESCRIPTION VARCHAR (50),
CONSERVATION_STATUS VARCHAR (50),
PRIMARY KEY (ID_DEVICE),
FOREIGN KEY (ID_ROOM) REFERENCES ROOMS (ID_ROOM)
);



CREATE TABLE CLASSES (
ID_CLASS INTEGER (5) NOT NULL,
SNO INTEGER (5),
DESCRIPTION VARCHAR (50),
PRIMARY KEY (ID_CLASS),
FOREIGN KEY (SNO) REFERENCES INSTRUCTOR (SNO)
);


CREATE TABLE INSTRUCTOR (
SNO INTEGER (5) NOT NULL,
FNAME VARCHAR (10),
LNAME VARCHAR (10),
PHONE CHAR (12),
DEGREE VARCHAR (50),
PROFESIONAL_EXP VARCHAR (200),
PRIMARY KEY (SNO)
);

CREATE TABLE MEMBER (
MEMBERSHIP_NUM INTEGER (5) NOT NULL,
M_NAME VARCHAR (10),
M_LNAME VARCHAR (10),
ADDRESS VARCHAR (40),
M_PHONE CHAR (12),
BANK_INFO VARCHAR (40),
PROFESSION VARCHAR (20),
PRIMARY KEY (MEMBERSHIP_NUM)
);

CREATE TABLE SQUASH_COURTS (
ID_SQUASH INTEGER (5) NOT NULL,
LOCATION VARCHAR (50),
S_CONDITION VARCHAR (20),
PRIMARY KEY (ID_SQUASH)
);


CREATE TABLE RESERVATION (
RESERVATION_ID INTEGER (5) NOT NULL,
ID_SQUASH INTEGER (5),
MEMBERSHIP_NUM INTEGER (5),
R_DATE DATE,
R_TIME TIME, 
PRIMARY KEY (RESERVATION_ID),
FOREIGN KEY (ID_SQUASH) REFERENCES SQUASH_COURTS (ID_SQUASH),
FOREIGN KEY (MEMBERSHIP_NUM) REFERENCES MEMBER (MEMBERSHIP_NUM)
);



CREATE TABLE ASSIGNED (
ID_ROOM INTEGER (5),
ID_CLASS INTEGER (5),
FOREIGN KEY (ID_ROOM) REFERENCES ROOMS (ID_ROOM),
FOREIGN KEY (ID_CLASS) REFERENCES CLASSES (ID_CLASS)
);





CREATE TABLE ATTEND (
ID_CLASS INTEGER (5),
MEMBERSHIP_NUM INTEGER (5),
FOREIGN KEY (ID_CLASS) REFERENCES CLASSES (ID_CLASS),
FOREIGN KEY (MEMBERSHIP_NUM) REFERENCES MEMBER (MEMBERSHIP_NUM)
);


CREATE TABLE ADMIN (
ID_ADMIN INTEGER(5) NOT NULL,
A_NAME VARCHAR (10),
A_LNAME VARCHAR (10),
A_USERNAME VARCHAR (20),
PASSSWORD VARCHAR (8),
PRIMARY KEY (ID_ADMIN)
);



